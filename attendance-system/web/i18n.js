// Current language state
window._lang = localStorage.getItem('attendance-lang') || 'vi';

/**
 * Get translated string by key.
 * @param {string} key
 * @returns {string}
 */
function t(key) {
  return (TRANSLATIONS[window._lang] || TRANSLATIONS['vi'])[key] || key;
}

/**
 * Apply all data-i18n attributes in the DOM.
 */
function applyI18n() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const attr = el.getAttribute('data-i18n-attr');
    if (attr) {
      el.setAttribute(attr, t(key));
    } else {
      el.textContent = t(key);
    }
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
  });
  // Update html lang attribute
  document.documentElement.lang = window._lang;
}

/**
 * Toggle between 'vi' and 'en'.
 */
function toggleLanguage() {
  window._lang = window._lang === 'vi' ? 'en' : 'vi';
  localStorage.setItem('attendance-lang', window._lang);
  applyI18n();
  updateLangToggleBtn();

  // If app.js is initialized and active, trigger re-renders
  if (typeof state !== 'undefined' && state.token) {
    if (typeof switchView === 'function') {
      switchView(state.activeView);
    }
    if (state.lastScan && typeof showLastScan === 'function') {
      showLastScan(state.lastScan);
    }
    if (typeof loadAll === 'function') {
      loadAll();
    }
  }
}

